    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        if (ID1.isSelected() && Data1.isSelected()){
            ID.setEnabled(true);
            Data.setEnabled(true);
            Nome.setEnabled(false);
            Email.setEnabled(false);
            pesquisar.setEnabled(true);

            quantidade = 2;
            selecionado1 = "id";
            selecionado2 = "data";

        }else if(ID1.isSelected()){
            ID.setEnabled(true);
            Data.setEnabled(false);
            Nome.setEnabled(false);
            Email.setEnabled(false);
            pesquisar.setEnabled(true);

            quantidade = 1;
            selecionado1 = "id";

        }else if(Nome1.isSelected() && Data1.isSelected()){
            ID.setEnabled(false);
            Data.setEnabled(true);
            Nome.setEnabled(true);
            Email.setEnabled(false);
            pesquisar.setEnabled(true);

            quantidade = 2;
            selecionado1 = "nome";
            selecionado2 = "data";

        }else if(Nome1.isSelected()){
            ID.setEnabled(false);
            Data.setEnabled(false);
            Nome.setEnabled(true);
            Email.setEnabled(false);
            pesquisar.setEnabled(true);

            quantidade = 1;
            selecionado1 = "nome";

        }else if(Email1.isSelected() && Data1.isSelected()){
            ID.setEnabled(false);
            Data.setEnabled(true);
            Nome.setEnabled(false);
            Email.setEnabled(true);
            pesquisar.setEnabled(true);

            quantidade = 2;
            selecionado1 = "email";
            selecionado2 = "data";

        }else if(Email1.isSelected()){
            ID.setEnabled(false);
            Data.setEnabled(false);
            Nome.setEnabled(false);
            Email.setEnabled(true);
            pesquisar.setEnabled(true);

            quantidade = 1;
            selecionado1 = "email";

        }else if(Data1.isSelected()){
            ID.setEnabled(false);
            Data.setEnabled(true);
            Nome.setEnabled(false);
            Email.setEnabled(false);
            pesquisar.setEnabled(true);

            quantidade = 1;
            selecionado1 = "email";

        }else{
            JOptionPane.showMessageDialog(this, "É preciso selecionar ao menos um tema de pesquisa.");
        }
        
        

    }//GEN-LAST:event_jButton1ActionPerformed

